    SelectBdemType<TYPE>::Type BdemType;

    typedef typename
    bslmf::If<bdltuxxx::UnsetValueIsDefined<BdemType>::VALUE,
             TypeHasBdetuNullValue,
             TypeHasNoBdetuNullValue>::Type Toggle;

